const fs = require('node:fs');
const path = require('node:path');

const rootDir = path.resolve(__dirname, '..');
const postsDir = path.join(rootDir, 'posts');
const outputFile = path.join(rootDir, 'js', 'post-markdown.js');

const markdownFiles = fs
  .readdirSync(postsDir, { withFileTypes: true })
  .filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
  .map((entry) => entry.name)
  .sort((left, right) => left.localeCompare(right));

const postMarkdown = {};

markdownFiles.forEach((fileName) => {
  const postId = path.basename(fileName, '.md');
  const markdown = fs.readFileSync(path.join(postsDir, fileName), 'utf8');
  postMarkdown[postId] = markdown;
});

const manifest = [
  '// Generated by scripts/generate-post-markdown.js.',
  '// Edit posts/*.md, then rerun `node scripts/generate-post-markdown.js`.',
  `window.POST_MARKDOWN = ${JSON.stringify(postMarkdown, null, 2)};`,
  '',
].join('\n');

fs.writeFileSync(outputFile, manifest, 'utf8');
